#include "Bin1D.h"


//______________________________________________________________________________

namespace Isis {

  //______________________________________________________________________________
  //
  Bin1D::Bin1D( const double &vmin ) : Bin(), fMin( vmin ) { }

  //______________________________________________________________________________
  //
  Bin1D::~Bin1D() { }

  //______________________________________________________________________________
  //
  void Bin1D::fill( const double &pos, const double &weight ) {
    
    if ( pos < fMin )
      fMin = pos;
    
    fEntries++;
    fSumOfWeights += weight;
  }

}
